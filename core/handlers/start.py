"""
Модуль стартовых хендлеров Telegram-бота.

Содержит:
- обработчик команды /start;
- callback-обработчик страницы с описанием возможностей бота;
- callback-обработчик возврата на стартовое сообщение.

Навигация между "страницами" реализована через inline-клавиатуры
и редактирование исходного сообщения методом `edit_text`.
"""

from aiogram import Router, F
from aiogram.filters import CommandStart
from aiogram.types import Message, CallbackQuery

from core.keyboards.inline_keyboards import start_keyboard, capabilities_keyboard

router = Router()

START_TEXT = (
    '🔍 Привет!\n\n'
    'Я – ваш *персональный* ассистент. '
    'Отправьте мне идентификационный номер (VIN) и я помогу его проверить.\n\n'
    '*Важно про точность данных*\n'
    'Я даю приблизительную справочную расшифровку VIN на основе встроенных таблиц соответствий. '
    'Точный состав и интерпретация некоторых полей могут отличаться у разных автопроизводителей и '
    'заводов, потому что внутренние правила маркировки зависят от бренда, рынка и конкретной модели. '
    'Поэтому результат бота стоит использовать для предварительной проверки, '
    'а для юридических или технических критичных задач сверять данные по официальным источникам '
    'производителя и документам автомобиля.\n\n'
    'С уважением, \n'
    '*Разработчик*: IK\n\n'
)
"""
Текст стартового сообщения бота.

Показывается пользователю по команде `/start`.
Содержит краткое описание назначения бота и предупреждение о том,
что расшифровка VIN носит справочный характер.
"""


@router.message(CommandStart())
async def cmd_start(message: Message) -> None:
    """
    Обрабатывает команду `/start`.

    Отправляет пользователю стартовое сообщение с кратким описанием бота
    и инлайн-клавиатурой для перехода к дополнительной информации
    о возможностях бота.

    :param message:
        Входящее сообщение пользователя с командой `/start`.
    :return:
        Ничего не возвращает. Отправляет ответное сообщение в чат.
    """

    await message.answer(
        START_TEXT,
        reply_markup=start_keyboard()
    )

@router.callback_query(F.data == 'what_can_you_do')
async def show_capabilities(callback: CallbackQuery) -> None:
    """
    Показывает пользователю страницу с возможностями бота.

    Хендлер вызывается при нажатии на inline-кнопку с callback_data
    `what_can_you_do`. Вместо отправки нового сообщения редактирует текущее,
    создавая эффект перехода на отдельную страницу интерфейса.

    На странице отображаются основные функции:
    - проверка соответствия VIN стандарту ISO-3779;
    - определение информации по WMI;
    - расшифровка модельного года;
    - проверка контрольного символа.

    :param callback:
        CallbackQuery, сформированный нажатием inline-кнопки.
    :return:
        Ничего не возвращает. Редактирует текущее сообщение и подтверждает callback.
    """

    await callback.message.edit_text(
        'ℹ️ *Что я умею*\n\n'
        'Я могу помочь со следующим:\n'
        '- Проверить соответствие VIN ISO-3779;\n'
        '- Определить информацию по WMI;\n'
        '– Расшифровать модельный год выпуска ТС;\n'
        '- Проверить контрольный символ.\n\n'
        '*ВАЖНО*\n'
        'Я пока ничего не знаю о транспортных средствах старше 2000 года :(',
        reply_markup=capabilities_keyboard()
    )
    await callback.answer()

@router.callback_query(F.data == 'back_to_start')
async def back_to_start(callback: CallbackQuery) -> None:
    """
    Возвращает пользователя на стартовую страницу бота.

    Хендлер вызывается при нажатии на inline-кнопку с callback_data
    `back_to_start`. Редактирует текущее сообщение, восстанавливая
    исходный стартовый текст и стартовую клавиатуру.

    :param callback:
        CallbackQuery, сформированный нажатием inline-кнопки "Назад".
    :return:
        Ничего не возвращает. Редактирует сообщение и подтверждает callback.
    """

    await callback.message.edit_text(
        START_TEXT,
        reply_markup=start_keyboard()
    )
    await callback.answer()
