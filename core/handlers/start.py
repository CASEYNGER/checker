from aiogram import Router
from aiogram.filters import CommandStart
from aiogram.types import Message

router = Router()

@router.message(CommandStart())
async def cmd_start(message: Message) -> None:
    await message.answer(
        'Привет!\n\n'
        'Я – ваш *персональный* ассистент. '
        'Отправьте мне идентификационный номер (VIN) и я помогу его проверить.\n\n'
        '*Важно про точность данных*\n'
        'Я даю приблизительную справочную расшифровку VIN на основе встроенных таблиц соответствий. '
        'Точный состав и интерпретация некоторых полей могут отличаться у разных автопроизводителей и '
        'заводов, потому что внутренние правила маркировки зависят от бренда, рынка и конкретной модели. '
        'Поэтому результат бота стоит использовать для предварительной проверки, '
        'а для юридических или технических критичных задач сверять данные по официальным источникам '
        'производителя и документам автомобиля.\n\n'
        'С уважением, \n'
        '*Разработчик*: IK'
    )
